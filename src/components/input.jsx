import React from "react";
import { useState } from "react";

/////////////////////////////////////////////////FUNCION FORMULARIO////////////////////////////////////////////////////////////////////////////77
const Form = (e) => {
  const [usuario, setUsuario] = useState("");
  const [contraseña, setContraseña] = useState("");
  const [isLoggedIn, setIsLoggedIn] = useState(false);

  const handleUsernameChange = (e) => {
    setUsuario(e.target.value);
  };

  const handlePasswordChange = (e) => {
    setContraseña(e.target.value);
  };

  const handleLogin = (e) => {
    e.preventDefault();
    setIsLoggedIn(true);
  };

  const handleLogout = () => {
    setIsLoggedIn(false);
  };
  ///////////////////////////////////////////////VALIDAR  SI ESTÁ LA SESIÓN ABIERTA/////////////////////////////////////////////////////////
  if (isLoggedIn) {
    return (
      <div>
        {/* ///////////////////////RETORNAR UN TEXTO QUE DIGA SESIÓN INICIADA//////////////////// */}
        <p>Sesión iniciada</p>
        {/* ///////////////////////RETORNAR UN BOTON CON EL TEXTO CERRAR SESIÓN////////////////// */}
        <button onClick={handleLogout}>Cerrar sesión</button>
      </div>
    );
  }
  /////////////////////////////////////////////////VALIDAR  SI ESTÁ LA SESIÓN ABIERTA/////////////////////////////////////////////////////////

  //////////////////////////////////////////////ELEMENTOS HTML///////////////////////////////////////////////////////////////////////////////
  return (
    <div>
      <form onSubmit={handleLogin}>
        <label htmlFor="usuario">Usuario:</label>
        <input
          type="text"
          className="usuario"
          placeholder="Nombre de Usuario"
          value={usuario}
          onChange={handleUsernameChange}
        />
        <br />
        <br />
        {/* /////////////////////////////////////INPUT PASSWORD/////////////////////////////////////////////////////////////////// */}
        <label htmlFor="contraseña">Contaseñas:</label>
        <input
          className="contarseña"
          type="password"
          placeholder="Contraseña"
          value={contraseña}
          onChange={handlePasswordChange}
        />
        {/* /////////////////////////////////////INPUT PASSWORD/////////////////////////////////////////////////////////////////// */}
        <br />
        <br />
        {/* ///////////////////////////////////////BOTON INICIAR SESIÓN/////////////////////////////////////////////////////////////// */}
        <button type="submit">Iniciar sesión</button>
        {/* ///////////////////////////////////////BOTON INICIAR SESIÓN/////////////////////////////////////////////////////////////// */}
        
      </form>
    </div>
  );
};
//////////////////////////////////////////////ELEMENTOS HTML///////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////EXPORTACIÓN////////////////////////////////////////////////////////////////////////////////////
export default Form;
