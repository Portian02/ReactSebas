import { useState } from "react";
import "./Input.css";

/////////////////////////////////////////////////FUNCION FORMULARIO////////////////////////////////////////////////////////////////////////////77
const Form = ({ onSubmit }) => {
  const [usuario, setUsuario] = useState("");
  const [contrasena, setContrasena] = useState("");

  ///////////////////////////////////////////////funciones de Context inputs/////////////////////////////////////////////////////////
  const handleUserChange = (e) => {
    setUsuario(e.target.value);
  };
  const handlePasword = (e) => {
    setContrasena(e.target.value);
  };
  const handleLogin = () => {
    if (usuario && contrasena) {
      onSubmit({ user: usuario, password: contrasena });
    }
  };

  /////////////////////////////////////////////////funciones de Context inputs/////////////////////////////////////////////////////////

  //////////////////////////////////////////////ELEMENTOS HTML///////////////////////////////////////////////////////////////////////////////
  return (
    <div className="container3">
      <h1>Iniciar Sesion</h1>
      <form onSubmit={handleLogin}>
        <input
          type="text"
          className="usuario"
          placeholder="Nombre de Usuario"
          value={usuario}
          onChange={handleUserChange}
        />
        <br />
        <br />
        {/* /////////////////////////////////////INPUT PASSWORD/////////////////////////////////////////////////////////////////// */}

        <input
          className="contarsena"
          type="password"
          placeholder="Contraseña"
          value={contrasena}
          onChange={handlePasword}
        />
        {/* /////////////////////////////////////INPUT PASSWORD/////////////////////////////////////////////////////////////////// */}
        <br />
        <br />
        {/* ///////////////////////////////////////BOTON INICIAR SESIÓN/////////////////////////////////////////////////////////////// */}
        <button onClick={handleLogin} type="submit" className="input">
          Iniciar sesión
        </button>
        {/* ///////////////////////////////////////BOTON INICIAR SESIÓN/////////////////////////////////////////////////////////////// */}
      </form>
    </div>
  );
};
//////////////////////////////////////////////ELEMENTOS HTML///////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////EXPORTACIÓN////////////////////////////////////////////////////////////////////////////////////
export default Form;
